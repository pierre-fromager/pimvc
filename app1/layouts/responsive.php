                        <h1>
                            <span class="fa fa-cutlery"></span>
                            Alimentation
                        </h1>
                        <h2>Le régime du chat</h2>
                        <h3>La base</h3>
                        <p>
                            Le chat est un carnivore et son régime alimentaire doit 
                            être constitué de viande à 80%.<br/>
                            Comme tous les félins le chat est un chasseur, à ce titre un 
                            jeux pour obtenir sa nourriture est une excellente stimulation.<br/>
                            Une source d'approvisionnement en eau est obligatoire.<br/>
                            Un apport ponctuel en lipide peut parfois être nécessaire.<br/>
                            Un apport en fibre est superflu, toutefois les légumes 
                            peuvent contribuer à l'apport en vitamine et fibre comme 
                            par exemple les haricots verts, carottes...<br/>
                            Eviter formellement les aliments contenant du 
                            lactose car ceci est indigeste et source de complication rénale.<br/>
                            A ce titre, le lait de vache est à proscrire, préférez un 
                            lait spécial chat que vous pourrez vous procurez facilement 
                            en grande distribution.
                        </p>
                        <h3>L'alternance</h3>
                        <p>
                            Votre chat tout comme vous, aime changer de nourriture 
                            tous les jours sinon il se lasse.<br/>
                            Selon le sexe de l'animal on relèvera une préférence pour 
                            le boeuf chez les mâles et le poisson chez les femelles.<br/>
                            Alterner thon-sardine et foie-coeur de boeuf est idéal, 
                            les viandes blanches et la volaille sont appréciées par 
                            tous (moyennant cuisson bouillie ou grillée).<br/>
                            Le fer étant la base de la constitution de l'hémoglobine, 
                            servir la viande rouge crue.<br/>
                            Les croquettes sont un poison qui est réalisé à base de 
                            plumes, de cailloux, d'os, d'ongles et d'autres composant n'apparaissant 
                            pas dans la composition.<br/>
                            Les sachets fraîcheurs sont constitués à 90% d'eau.<br/>
                            Les boîtes demeurent une solution de substitution uniquement.<br/>
                            Sachant que vous ne pourrez pas au quotidien éviter les 
                            sachets,les boîtes et les croquettes, je recommanderais 
                            la marque Hill's uniquement (coté croquette).<br/>
                            Pour information tout aliment mixé perd 80% de sa valeur 
                            vitaminée (principe d'oxydo-réduction), préférez la 
                            découpe au ciseau.
                        </p>
