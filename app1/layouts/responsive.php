            <section class="sante">
                <div class="container">
                    <div class="row">
                        <h1>
                            <span class="fa fa-heartbeat"></span>
                            Santé
                        </h1>
                        <h3>Les bonnes habitudes</h3>
                        <p>
                            Se déchausser avant de circuler dans votre habitation de 
                            façon à éviter le transport des germes et bactéries provenant de l'extérieur.<br/>
                            Avant de soigner un animal lavez vous les mains.<br/>
                            Pour désinfecter une plaie préférez la <i>Béthadine</i> 
                            aux solutions à base d'alcool, à moins que votre animal 
                            réagisse mal à l'iode.<br/>
                            Sur une plaie sèche utiliser la <i>Béthadine jaune</i>, 
                            sur une plaie ouverte la <i>Béthadine rouge (scrub)</i>.<br/>
                            N'utilisez des antibiotiques que sur prescription de votre vétérinaire.<br/>
                            Ne pas utiliser de médicament pour l'humain sur votre animal.<br/>
                            Suivez la durée de la prescription d'antibiotiques jusqu'à 
                            la fin même si votre animal se porte mieux.<br/>
                            Respectez les posologies quelque soit le médicament et 
                            lisez la notice aux rubriques contre-indication et 
                            effets indésirables.
                        </p>
                        <h3>La veille</h3>
                        <p>
                            Votre chat mange, joue,il est câlin; il est en pleine forme.<br/>
                            Votre chat ne se nourrit plus, ne joue plus, manque 
                            d'élasticité, perte de tonus; alors consultez sans attendre.<br/>
                            Un animal cantonné à l'intérieur de votre lieux d'habitation 
                            a 70% de chance de moins de ne pas tomber malade.<br/>
                            Si votre bestiole au sein d'une fratrie présente des signes infectieux afin 
                            d'éviter la pandémie, pratiquez l'isolement le temps du traitement.<br/>
                            Surveillez régulièrement les oreilles, la gueule, l'anus 
                            pour détecter tout signe d'inflammation.
                        </p>
                        <h3>Les petits bobos</h3>
                        <p>
                            En cas de diarrhée vérifier la présence de sang dans les selles.<br/>
                            Pour arrêter une diarrhée sans hématies, faire un bouillon de légume avec 
                            du riz blanc (ou pattes ou pomme de terre) et lui donner 
                            uniquement le jus 3 fois par jour pendant 2 jours, améliorer avec du 
                            poulet sans peau afin d'éviter les matières grasses.<br/>
                            Lui donner 1/4 de yogourt nature par jour pour recoloniser 
                            la flore intestinale.<br/>
                            Entre autre avant de vous faire un sang d'encre, il est 
                            tout a fait possible que le régime alimentaire prescrit 
                            ne corresponde pas à l'animal.<br/>
                            Dans ce cas, orientez le progressivement vers le nouveau 
                            régime.<br/>
                            Inversement si votre petit roudoudou couine comme un 
                            malheureux quand il va a la selle c'est qu'il manque de 
                            fibres.<br/>
                            La solution sera de mélanger 20% de haricots verts à sa pâté.<br/>
                            Eventuellement, une base lipidique fera ponctuellement l'affaire 
                            (1/2 de cuillère à café) d'huile d'origine végétale.
                        </p>
                        <h3>La trousse du parfait secouriste</h3>
                        <ul>
                            <li>5 compresses stériles.</li>
                            <li>1 paire de gant en latex ou +.</li>
